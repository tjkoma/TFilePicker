package com.easyuse.tfilepicker

import android.content.Context

class TFilePicker {

    private val tFilePicker: TFilePicker = TFilePicker()

    enum class SelectMode {
        SINGLE, MULTI
    }

    private fun init(context: Context): TFilePicker {
        return tFilePicker
    }

    private fun single(): TFilePicker {
        return tFilePicker
    }

    private fun build(): TFilePicker {
        return tFilePicker
    }

}